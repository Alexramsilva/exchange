# ---------------------------
# FUNCIN PARA DESCARGAR FIX
# ---------------------------
@st.cache_data
def load_fix_data():
    start_date = "2015-01-01"
    end_date = datetime.now().strftime("%Y-%m-%d")

    url = f"https://www.banxico.org.mx/SieAPIRest/service/v1/series/SF43718/datos/{start_date}/{end_date}"
    headers = {"Bmx-Token": BANXICO_TOKEN}

    response = requests.get(url, headers=headers)

    if response.status_code != 200:
        return None

    datos = response.json()
    serie = datos["bmx"]["series"][0]["datos"]

    df = pd.DataFrame(serie)
    df["fecha"] = pd.to_datetime(df["fecha"], dayfirst=True)
    df["dato"] = pd.to_numeric(df["dato"], errors="coerce")

    return df


# ---------------------------
# FUNCIN PARA DESCARGAR DLAR INDEX
# ---------------------------
@st.cache_data
def load_dxy_data():
    ticker = "DX-Y.NYB"
    df = yf.download(ticker, period="2y", interval="1d")
    return df["Close"]


# ---------------------------
# GRFICO 1: TIPO DE CAMBIO FIX
# ---------------------------
