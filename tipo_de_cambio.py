    ax1.set_title("Tipo de Cambio FIX (MXN/USD)")
    ax1.set_xlabel("Fecha")
    ax1.set_ylabel("Tipo de Cambio")
    ax1.grid()
    ax1.legend()

    st.pyplot(fig1)

